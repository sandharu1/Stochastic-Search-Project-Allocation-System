    private void jPrintBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPrintBtnActionPerformed
        // TODO add your handling code here:
        //Print jtable preview directly using system printers
        MessageFormat header = new MessageFormat("Stochastic Project Allocation");
        MessageFormat footer = new MessageFormat("--IdeaFactory--                           page {0}");
        try {
            /* print the table */
            boolean complete = jXPreviewTbl.print(JTable.PrintMode.FIT_WIDTH, header, footer, true, null, true, null);

            /* if printing completes */
            if (complete) {
                /* show a success message */
                JOptionPane.showMessageDialog(this,
                        "Printing Complete",
                        "Printing Result",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                /* show a message indicating that printing was cancelled */
                JOptionPane.showMessageDialog(this,
                        "Printing Cancelled",
                        "Printing Result",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (PrinterException pe) {
            /* Printing failed, report to the user */
            JOptionPane.showMessageDialog(this,
                    "Printing Failed: " + pe.getMessage(),
                    "Printing Result",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jPrintBtnActionPerformed


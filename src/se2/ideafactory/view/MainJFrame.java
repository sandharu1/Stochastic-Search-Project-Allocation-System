        String[][] row = new String[200][2];
        jGenarateBtn.setEnabled(false);
        jSaveBtn.setEnabled(true);
        jCancelBtn.setEnabled(true);
        jPrintBtn.setEnabled(true);

        //Get algorithm selection
//        JRadioButton selectedButton = null;
